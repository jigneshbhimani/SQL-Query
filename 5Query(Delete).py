# 5.SQL DELETE Query:

# DELETE query is used to delete existing records in a table.

# DELETE Syntax:
'''
DELETE FROM table_name WHERE condition;
'''



# SQL DELETE Example:
'''
DELETE FROM Customers WHERE CustomerName='Jignesh';
'''
# SQL query deletes the customer "Jignesh" from the "Customers" table



# Delete All Records:
'''
DELETE FROM table_name;
'''


